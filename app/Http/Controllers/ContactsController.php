    /**
     * Store a newly created resource in storage.
     *
     * @return \Illuminate\Http\Response
     */
    public function store()
    {
        $params = $this->validate(request(), [
            'fullname' => 'required',
            'email'    => 'required|email',
            'message'  => 'required',
        ]);

        $contact = new Contact();
        $contact->fill($params);
        $contact->save();

        return redirect()->route('home');
    }

